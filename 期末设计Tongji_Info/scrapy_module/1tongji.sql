16. 关于毕业班学生选课的说明。请毕业班学生认真对照专业培养方案进行毕业资格自审，根据培养方案对各类学分及课程的要求、对照自己成绩及选课结果，认真核实梳理，对所缺失的课程及时安排补修，以免因疏忽无法正常毕业。

