4.   选课中出现问题的解决办法。同济大学教学管理信息系统支持的浏览器为谷歌Chrome，请同学们务必使用该浏览器，若用其他浏览器可能出现数据问题。若忘记登录密码，或出现网络问题请联系信息中心（65989006）。若有选课疑问，请务必在选课开放期间及时与自己学院教务科联系，仍无法解决的问题请联系本科生院学务管理中心（65983963）。

5.   关于免修课程的选课。关于免修课程的选课。学生申请免修且通过审核的课程也要正常选课，但不用修读。申请免修或免听的学生需要在新系统的成绩管理菜单中进行线上申请。

6.   关于欠缴学费的学生选课。欠缴学费的学生（已办理缓交手续的除外）不能参加第一、二轮选课，请到财务处使用自助缴费机或登录paycwc.tongji.edu.cn/payment先完成缴费，不接受现场缴费。若有贷款等问题请联系学院。

7.   关于不及格学分过多的学生选课。不及格学分超过25学分低于40学分的学生，选新修课程不得超过10学分；不及格学分超过40学分的学生，选新修课程不得超过5学分。

8.   关于“筛选”的说明。第一轮选课将主要根据专业课表进行筛选，筛选后已经选满的班级，第二轮不能再选，被筛选掉的学生要在第二轮选择该课程容量未满的其他班级或其他课程。由于选课过程中班级容量余额瞬息万变，建议对已选成功的必修课要慎重考虑改选，尤其是公共课较多的低年级学生。

9.   关于“选课备注”的说明。请不要选择选课备注里类似“限体育生”、“限民族班”、“限留学生”、“限..”等与自己不相干的课程。

10. 关于体育课的说明。前三轮选课只开放给当学期有体育课计划的学生，第四轮开放给全体学生，包括之前学期有漏选体育课的学生。

11. 关于英语课的说明。大学英语课程由必修课和选修课两部分组成。在学期间需完成6学分学习任务。规定如下：①尽量在第1、2、3学期内选读，每学期限选一门；②根据新生入学分级结果，编入四级或五级的学生，须修读必修课程“大学英语四级”或“大学英语五级”，以及两门选修类课程；③获半免资格的学生须选读指定选修课程中的两门；④编入大学英语A类的学生，须修读必修课程“大学英语A1”、“大学英语 A2”、“大学英语 A3”；⑤因特殊原因需申请调级的学生，请到外国语学院（同文楼）教务科办理相关手续。

12. 关于通识选修课的说明。2018级及以后学生：①学生在培养期间至少修满8或10个通识选修课学分（四年制专业8学分，五年制专业10学分）；②至少修读一门精品类通识选修课；③2019级开始至少修读一门艺术类通识选修课；④必须修读2学分创新创业通识选修学分；⑤每个模块累计最多选修两门课程（修满通识课学分后不再受此限制）。

13. 关于荣誉课程的说明。荣誉课程是为荣誉计划学生开设的课程，非荣誉计划学生不能修读。

14. 关于“选课申请”的说明。第三轮选课时，学务管理中心会开放英语课、体育课等少部分课程，供未选到这些课程的学生在系统中提交“选课申请”。相关学院将在第三轮选课结束后（可能会延续到下学期第2周）进行处理，处理结果包括代理选课或撤销申请，请同学们务必随时查看“我的课表”，以“选课申请”处理后的课表为准。对于平行班级仍有容量课程的选课申请将予以撤销，请学生自行调整课表。已经选了一门新修英语课后再提交的英语课选课申请将被撤销。如发现选课申请未处理或处理有问题，请在下学期第2周结束前联系学务管理中心，逾期不再受理、修改。

15. 关于“课表”的说明。为避免误操作，选课结束后请务必查询“我的课表”，确认自己最终的选课结果（包括“选课申请”的处理结果），查询“我的课表”的方法为：进入选课界面→选课管理→学生选课→我的课表。另外，请尽量根据“专业课表”安排的时间进行选课,查询“专业课表”的方法为：进入选课界面→选课管理→学生选课→专业课表。注意“我的课表”和“专业课表”是有区别的，具体可咨询自己学院教务科或本科生院。

16. 关于跨学科选课的说明。学校在第四轮选课中，将向学有余力的学生开放“跨学科互选课程” （重修课如果课号变化请联系学院教务员做课程替代，不能通过跨学科申请选课，否则影响毕业审核！！！）。流程为：学生提交跨学科课程申请，所在院系和开课院系审核通过后，学生再通过“个人选课”模块正常选课，具体要求及安排另行通知。

17. 关于毕业班学生选课的说明。请毕业班学生认真对照专业培养方案进行毕业资格自审，根据培养方案对各类学分及课程的要求、对照自己成绩及选课结果，认真核实梳理，对所缺失的课程及时安排补修，以免因疏忽无法正常毕业。

18. 关于记载成绩的说明。选课一旦完成，必须承担起认真修读课程的责任。选课数据是期末排考和成绩记载的唯一数据来源，选了课不参加考试则该课程为“旷考”；没选过课却参加了考试，成绩也不予承认。另外，从2017年9月开始，真实、完整地记载考试成绩。（详见附录1）

19. 本通知由本科生院负责解释。

